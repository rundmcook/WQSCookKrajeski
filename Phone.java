public class Phone extends ElectronicsItem{
    //Attributes
    private double cameraQuality;

    //constructor

    /**
     * Constructor for the Phone object
     * @param name name of the item
     * @param serial serial number for the item
     * @param price price of the item
     * @param brand brand of the item
     * @param model or version of the item
     * @param year release date of item
     * @param cameraQuality quality of the phone's camera in megapixels
     */
    public Phone(String name, int serial, double price, String brand, int quantity, String model, int year, double cameraQuality){
        super(name, serial, price, brand, quantity, model, year);
        this.cameraQuality = cameraQuality;
    }

    //accessors

    /**
     * accessor for the cameraQuality attribute
     * @return cameraQuality of the object
     */
    public double getCameraQuality(){
        return cameraQuality;
    }
    //mutators

    /**
     * Sets a new value for cameraQuality
     * @param cameraQuality new cameraQuality of the object
     */
    public void setCameraQuality(double cameraQuality){
        this.cameraQuality = cameraQuality;
    }
}